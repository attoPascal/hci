    <h1>Allgemeines zum Studium</h1>
    
<h2>§ 1 Qualifikationsprofil und Studienziele</h2>
<h3>(1) Studienziele</h3>
<p>Das Ziel des Bachelorstudiums Informatik an der Universität Wien ist die Vermittlung von Grundlagen
der Informatik und ihren Anwendungen in speziellen Ausprägungsfächern.</p>
<h3>(2) Qualifikationsprofil</h3>
<p>Das Bachelorstudium Informatik an der Universität
Wien soll eine wissenschaftlich geprägte Ausbil-
dung vermitteln, die Theorie, Fachwissen und praktische Kenntnisse der Informatik einschließt. Es
soll die Studierenden in die Lage versetzen, Me
thoden und Werkzeuge der
Informatik anzuwenden
sowie sich eigenständig an ihrer Erforsch
ung und Weiterentwicklung zu beteiligen.
Absolventinnen und Absolventen sind in der Lage, informatische Methoden, Vorgehensmodelle,
Werkzeuge und Systeme der Informat
ik zur Lösung praxisrelevanter
Probleme anzuwenden. Sie besit-
zen vertiefte Kenntnisse über die Implementierung und Validierung komplexer informatischer Syste-
me zur Information, Kommunikation und Steuerung und können diese in verschiedenen Anwen-
dungsbereichen einsetzen bzw. deren Einsatz leiten. Sie sind geschult, Algorithmen zu realisieren und
bezüglich ihrer Eigenschaften einzuschätzen und zu bewerten. Sie können im Team komplexe Soft-
waresysteme entwickeln, sie kennen die Anforderunge
n beim Arbeiten in Gruppen, sowie die Fähigkeit
zu verantwortlichem und verantwortungsbewusstem Handeln im Beruf.</p>
Darüber hinaus besitzen sie vertiefte Kenntnisse in einem der folgenden Ausprägungsfächer:
<ul>
<li>Bioinformatik (Biologie)</li>
<li>Medieninformatik (Medien- und Kommunikationswissenschaften)</li>
<li>Medizininformatik (Medizin)</li>
<li>Scientific Computing (Formal- und Naturwissenschaften)</li>
</ul>
und besitzen die Fähigkeit zur Konzipierung und Umsetzung von Lösungen zu gegebenen Problemstel-
lungen im Schnittfeld zwischen Informatik und Ausprägungsfach.

<h3>(3) Definition der Ausprägungsfächer</h3>
<h4>Ausprägungsfach Bioinformatik</h4>
<p>Die Bioinformatik ist eine Wissenschaft, die Modelle, Techniken und Methoden der Informatik in spe-
zifischen Fachgebieten der Biologie, wie Genetik, Molekularbiologie, Pharmazie, etc. anwendet. Absol-
ventInnen besitzen die Befähigung in enger Zusamm
enarbeit mit Biologen, Genetikern und anderen
Spezialisten aus den Lebenswissenschaften Aufgabenstellungen zur Simulation und Berechnung bio-
logischer Experimente un
d Daten durchzuführen.</p>
<h4>Ausprägungsfach Medieninformatik</h4>
<p>AbsolventInnen erlangen zusätzlich zur grundlegenden Informatikausbildung eine Ausbildung im
gewählten Anwendungsfeld Medien- und Kommunikationswissenschaften, so dass sie in interdis-
ziplinären Teams an interessanten und aktuellen
Fragestellungen der Medien
informatik mitarbeiten
können.
Curriculum für das Bachelorstudium Informatik (Version 2011) – Stand: Oktober 2011
Rechtsverbindlich sind allein die im Mitteilungs
blatt der Universität Wi
en kundgemachten Texte.</p>

<p>Das Ausprägungsfach Medieninformatik umfasst
Lehrveranstaltungen aus den Anwendungsfeldern
wie z.B. Medienpädagogik und Kommunikationswissenschaften sowie LV zu jenen Technologien und
Methoden die nötig sind, um Problemstellungen aus diesen Einsatzbereichen zu bearbeiten und ent-
sprechende Lösungen zu entwickeln.</p>
<h4>Ausprägungsfach Medizininformatik</h4>
<p>AbsolventInnen besitzen die Fähigkeit, in den vielfältigen Bereichen der Medizin und des Gesund-
heitswesens in interdisziplinärer Zusammenarbeit
mit ÄrztInnen und Verantwortlichen des Gesund-
heitswesens Projekte erfolgreich auszugestalten und durchzuführen. Dazu erwerben sie neben ihrer
Informatik-Kompetenz Wissen über medizinische und klinische Bedürfnisse, Fragestellungen und
Prozeduren, sowie Kenntnisse, Fähigkeiten und Fert
igkeiten zur Verarbeitung medizinischer Daten,
Bilder und Informationen und zur Un
terstützung medizinischer Abläufe.</p>
<h4>Ausprägungsfach Scientific Computing</h4>
<p>In allen Naturwissenschaften spielt heute die Inform
atik in der Forschung und Entwicklung eine zent-
rale Rolle. Dies umfasst die Berechnung von theoretischen Modellen, die Analyse von Daten aus Expe-
rimenten, und die Durchführung von Computerexperimenten und Simulationen. Das Bachelorstudi-
um in der Ausprägung Scientific Computing soll die
AbsolventInnen dazu qualifizieren in interdis-
ziplinären Forschungsteams bei der Lösung solcher Fragestellungen mitzuarbeiten.</p>

<h3>(4) Innovative Lehrkonzepte</h3>
Studierende werden zwecks Intensivierung/Verbesse
rung der Betreuung/Intera
ktion zusätzlich durch
erfahrene KollegInnen betreut, die mit dem jeweiligen Lehr/Lernkonzept vertraut sind und persönli-
che wie auch online Beratung zu spezifischen Lehrveranstaltungen anbieten.
Im Studium wird besonderer Wert
auf projektbasiertes Lernen gelegt. Dieses umfasst nach einer An-
leitungsphase selbstgesteuertes und weitgehend selb
storganisiertes Lernen. Projekte zielen verstärkt
auf Teamarbeit und Interaktion ab, die teils in direktem Kontakt, teils computerunterstützt erfolgt. Die
reflektierte Zusammenarbeit in Projektteams soll St
udierende an die berufliche wie auch wissenschaft-
liche Praxis heranführen.
Durch die Ausrichtung des Studiums auf Ausprägungs
fächer werden Studierende ebenfalls an die Ar-
beit in interdisziplinären, heterogenen Teams vorb
ereitet. In das Lehrangebot werden Lehrveranstal-
tungen integriert, die metafachliche Kompetenzen, insbesondere Kommunikation und Teamkompe-
tenz und deren Transfer in
das Berufsumfeld fördern.
In den einzelnen Lehrveranstaltungen wird angestrebt, einen von den Lehr/Lernzielen abhängigen
und den Bedürfnissen der Beteiligten entsprechenden effektiven Mix von Präsenz- und Online-
Elementen anzubieten.
<h2>§ 2 Dauer und Umfang</h2>
<p>Der Arbeitsaufwand für das Bachelorstudium Inform
atik beträgt 180 ECTS-Pun
kte (abgekürzt: ECTS).</p>
Das entspricht einer vorgesehenen Studiendauer von sechs Semestern.
<h2>§ 3 Zulassungsvoraussetzungen</h2>
Voraussetzung für die Zulassung zum ordentlichen
Bachelorstudium Informatik an der Universität
Wien ist die allgemeine Universitätsreife und die Kenntnis der deutschen Sprache.
<h2>§ 4 Akademischer Grad</h2>
Absolventinnen bzw. Absolventen des Bachelorstudiums Informatik ist der akademische Grad „Bache-
lor of Science“ – abgekürzt „BSc“
– zu verleihen. Im Falle der Führung ist dieser akademische Grad
dem Namen nachzustellen.
Die Spezifikation des Ausprägungsfaches („Bioinfor
matik“, „Medieninformatik“, „Medizininformatik“
oder „Scientific Computing“) wird im Verleihungsbescheid angegeben. 

